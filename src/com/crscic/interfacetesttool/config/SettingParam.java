public class SettingParam
{
	private String name;
	private String quoteSrc;
	private String quoteType;
	private String value;
	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}
	/**
	 * @return the quoteSrc
	 */
	public String getQuoteSrc()
	{
		return quoteSrc;
	}
	/**
	 * @param quoteSrc the quoteSrc to set
	 */
	public void setQuoteSrc(String quoteSrc)
	{
		this.quoteSrc = quoteSrc;
	}
	/**
	 * @return the quoteType
	 */
	public String getQuoteType()
	{
		return quoteType;
	}
	/**
	 * @param quoteType the quoteType to set
	 */
	public void setQuoteType(String quoteType)
	{
		this.quoteType = quoteType;
	}
	/**
	 * @return the value
	 */
	public String getValue()
	{
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value)
	{
		this.value = value;
	}
	
}
